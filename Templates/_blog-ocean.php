                <h2>Ways You Can Help To Protect The Ocean During The Decade of Ocean Science</h2>
                <p>Did you know that 2021 marks the first year in the United Nations Decade of Ocean Science for Sustainable Development? According to the United Nations, oceans cover three-quarters of the earth's surface and 97% of the earth's water. Unfortunately, "roughly 80 percent of marine and coastal pollution originates on land- including agricultural run-off, pesticides, and plastic pollution".</p>
                <p>So what can you and your family do to help protect the ocean this year, and the years to come? </p>
                <p>1) Eat sustainable seafood! We know it can be hard to know what fish are sustainable and okay to eat, so we recommend learning more about the eco-certification of seafood and supporting small, well-managed, wild fisheries. You can also choose to eat fish that are abundant, rather than choosing species that are at risk of being overfished.</p>
                <p>2) Reduce your single-use plastic intake, and reuse items whenever you can! We recommend encouraging you and your family to find new uses for used or old items, like turning old clothes into pillowcases or making art with any old items! It's also a great way to encourage creativity and off-screen time while teaching your family another way to recycle. Choosing reusable items is also a great way to help protect our oceans from plastic pollution.</p>
